    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        String nome = iNome.getInputText();
        Logradouro logradouro = (Logradouro) iSelectLogradouro.getSelectedItem(); 
        String celular = iCelular.getInputText();
        String telefone = iTelefone.getInputText();
        String cnpj = iCnpj.getInputText();
        String ie = iIe.getInputText();
        String diretorio = iDiretorio.getInputText();
        
        if(new EmpresaControl().save(empresa_id, nome, telefone, celular, logradouro, cnpj, ie, diretorio)){
            this.success = true;
            
            components.Message.show(null,"Dados da empresa registrados com sucesso.");
            
        }else{
            this.success = false;
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

