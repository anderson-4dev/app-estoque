    //carrega o formulario com dados em caso de edi√ß√£o
    public void loadForm(int id){
        
        Empresa e = new EmpresaDao().get(id);
        empresa_id = id;
        iNome.setInputText(e.getNome());
        iCelular.setInputText(e.getCelular());
        iTelefone.setInputText(e.getTelefone());
        iCnpj.setInputText(e.getCnpj());
        iIe.setInputText(e.getIe());
        iDiretorio.setInputText(e.getDiretorio());
        
        iSelectLogradouro.setSelectedItem((Object) e.getLogradouro());
        
        this.txtDateUpdate.setText("Atualiza√ß√£o em "+Helpers.DataHora.fomartStringDateTime(e.getRegistro()));
        
        
    }
