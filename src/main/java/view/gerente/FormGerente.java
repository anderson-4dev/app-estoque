    private void loadJtable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();

        modelo.setRowCount(0);

        gerentes.clear();
        GerenteControl control = new GerenteControl();
        
        control.getAll().forEach(p -> {
            gerentes.add(p);
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                (p.isBloqueado()) ? "Bloqueado" : "Normal"
            });
        });
        
        //uControl.closeConection();
    }
    
    private void cancelForm() {
        //desabilita a entrada de dados na input
        iCodigo.readOnly(true);
        iCodigo.setInputText("");
        
        iNome.readOnly(true);
        iNome.setInputText("");
        
        iSenha.readOnly(true);
        iSenha.setInputText("");
        
        iDesconto.readOnly(true);
        iDesconto.setInputText("0,00");
        
        iLimite.readOnly(true);
        iLimite.setInputText("0,00");
        
        iUser.readOnly(true);
        iUser.setSelectedIndex(0);
        
        iCk_excederDesconto.setSelected(false);
        iCk_excederLimite.setSelected(false);
        iCk_liberarVendas.setSelected(false);
        iCk_cancelarVenda.setSelected(false);
        
        this.btnSalvar.setEnabled(false);
        this.btnDeletar.setEnabled(false);
        this.btnNovo.setEnabled(true);
        
        this.jTable.setEnabled(true);
        this.jTable.requestFocus();
        this.btnDeletar.setText("BLOQUEAR");
        this.btnCancelar.setText("FECHAR"); 
    }
    
    private void newForm() {
        //desabilita a entrada de dados na input
        iSenha.readOnly(false);
        
        iDesconto.readOnly(false);
        
        iLimite.readOnly(false);
        
        iUser.readOnly(false);
        
        iNome.readOnly(false);
        
        iNome.requestFocus();
        
        this.btnNovo.setEnabled(false);
        this.btnSalvar.setEnabled(true);
        this.btnDeletar.setEnabled(false);
        
        this.jTable.setEnabled(false);
        
        this.btnCancelar.setText("CANCELAR");
        
    }
    
    private void editForm(){
        this.newForm();
        this.btnDeletar.setEnabled(true);
    }
    
    //carregar o formulario para edição
    private void loadForm() {
        int index =  jTable.getSelectedRow();
        if(index > -1){
            Gerente u = gerentes.get(index);
            this.editForm();
            
            this.iCodigo.setInputText(u.getId());
            this.iNome.setInputText(u.getNome());
            iSenha.setInputText(u.getSenha());
            iDesconto.setInputText(u.getDescontoMaximo());
            iLimite.setInputText(u.getLimiteMaximo());
            
            iUser.setSelectedItem((Object) u.getUsuarioVinculado());
            
            iCk_excederDesconto.setSelected(u.isExcederDesconto());
            iCk_excederLimite.setSelected(u.isExcederLimite());
            iCk_liberarVendas.setSelected(u.isLiberarTodasVendas());
            iCk_cancelarVenda.setSelected(u.isCancelarVenda());
            
            if(u.isBloqueado()){
                this.btnDeletar.setText("DESBLOQUEAR");
            }
        }
        
    }
