    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        int id = iCodigo.getValue();
        String nome = iNome.getInputText();
        String senha = iSenha.getInputText();
        double descontoMaximo = iDesconto.getValue();
        double limiteMaximo = iLimite.getValue();
        Usuario usuarioVinculado = (iUser.getSelectedIndex()== 0) ? null : (Usuario) iUser.getSelectedItem();
        boolean excederDesconto = iCk_excederDesconto.isSelected();
        boolean excederLimite = iCk_excederLimite.isSelected();
        boolean liberarTodasVendas = iCk_liberarVendas.isSelected();
        boolean cancelarVenda = iCk_cancelarVenda.isSelected();

        GerenteControl control = new GerenteControl();

        if(control.save(id, nome, senha, descontoMaximo, limiteMaximo, usuarioVinculado, excederDesconto, excederLimite, liberarTodasVendas, cancelarVenda)){
            this.success = true;

            components.Message.show(null,"Gerente registrado com sucesso.");
            this.loadJtable();
            this.cancelForm();

        }else{
            this.success = false;
        }

        control.closeConection();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(this.btnSalvar.isEnabled()){//se o botão salvar estiver ativado
            this.cancelForm();
        }else{
            this.dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int id = iCodigo.getValue();
        if(id > 0){
            GerenteControl control = new GerenteControl();
            if(control.toggleAtive(id)){
                this.success = true;
                this.loadJtable();
                this.cancelForm();
            }else{
                components.Message.show(null, "Falha ao registrar!");
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

