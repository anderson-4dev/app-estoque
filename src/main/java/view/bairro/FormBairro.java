    private void loadJtable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable.getModel();

        modelo.setRowCount(0);

        bairros.clear();
        
        BairroControl control = new BairroControl();
        
        control.getAll().forEach(p -> {
            bairros.add(p);
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getCidade().toString()
            });
        });
        
        control.closeConection();
    }

    private void cancelForm() {
        //desabilita a entrada de dados na input
        iNome.enableDataInput(false);
        iCodigo.setInputText("");
        isEstados.readOnly(true);
        isEstados.setSelectedIndex(0);
        
        this.btnSalvar.setEnabled(false);
        this.btnDeletar.setEnabled(false);
        this.btnNovo.setEnabled(true);
        
        this.jTable.setEnabled(true);
        this.jTable.requestFocus();
        this.btnCancelar.setText("FECHAR"); 
        
    }
    
    private void newForm() {
        //desabilita a entrada de dados na input
        iNome.enableDataInput(true);
        isEstados.readOnly(false);

        iNome.requestFocus();
        this.btnNovo.setEnabled(false);
        this.btnSalvar.setEnabled(true);
        this.btnDeletar.setEnabled(false);
        
        this.jTable.setEnabled(false);
        
        this.btnCancelar.setText("CANCELAR");
        
    }
    
    private void editForm(){
        this.newForm();
        this.btnDeletar.setEnabled(true);
    }
    
    //carregar o formulario para edição
    private void loadForm() {
        int index =  jTable.getSelectedRow();
        if(index > -1){
            Bairro u = bairros.get(index);
            this.editForm();
            
            this.iCodigo.setInputText(u.getId());
            this.iNome.setInputText(u.getNome());
            
            this.isEstados.setSelectedItem((Object) u.getCidade());
        }
        
    }

    private void loadSelect() {
        //this.cidades = new CidadeControl().getAll();
        isEstados.loadOptions((List<Object>) (Object) new CidadeControl().getAll());
    }
