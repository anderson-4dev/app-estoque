    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        int id = iCodigo.getValue();
        
        String nome = iNome.getInputText();
        
        Cidade cidade = (Cidade) isEstados.getSelectedItem();
        
        BairroControl control = new BairroControl();
        
        if(control.save(id, nome, cidade)){
            this.success = true;

            components.Message.show(null,"Bairro registrado com sucesso.");
            this.loadJtable();
            this.cancelForm();

        }else{
            this.success = false;
        }
        
        control.closeConection();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if(this.btnSalvar.isEnabled()){//se o botão salvar estiver ativado
            this.cancelForm();
        }else{
            this.dispose();
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int id = iCodigo.getValue();
        if(id > 0){
            BairroControl control = new BairroControl();
            if(control.delete(id)){
                components.Message.show(null, "Bairro excluido da base de dados!");
                this.success = true;
                this.loadJtable();
                this.cancelForm();
            }else{
                components.Message.show(null, "Bairro não pode ser excluido!");
            }
            control.closeConection();
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

