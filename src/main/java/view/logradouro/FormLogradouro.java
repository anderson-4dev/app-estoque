    //carrega o formulario com dados em caso de edição
    public void loadForm(Logradouro l){
        iCodigo.setInputText(l.getId());
        iLugar.setInputText(l.getLogradouro());
        iCep.setInputText(l.getCep());
        
        isBairro.setSelectedItem((Object) l.getBairro());


        this.setTitle("Editar de Logradouro");
        this.btnCancelar.setText("FECHAR");
        //habilitar o botão de deletar
        this.btnDeletar.setEnabled(true);
    }
