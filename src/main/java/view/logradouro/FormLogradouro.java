    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int id = iCodigo.getValue();
        String cep = iCep.getInputText();
        String logradouro = iLugar.getInputText();
        Bairro bairro = (Bairro) isBairro.getSelectedItem(); 

        
        if(new LogradouroControl().save(id, cep, logradouro, bairro)){
            this.success = true;
            
            components.Message.show(null,"Logradouro registrado com sucesso.");
            
            //quando for produto novo que foi salvo, ele fecha a janela do formulario.
            if(id==0){
                this.dispose();
            }
            
        }else{
            this.success = false;
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        int id = iCodigo.getValue();
        
        if(new LogradouroControl().delete(id)){
            components.Message.show(null, "Logradouro excluido da base de dados!");
            this.success = true;
            this.dispose();
        }else{
            components.Message.show(null, "Logradouro com movimentação, não pode excluir!");
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

